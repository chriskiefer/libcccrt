bool arma_eq(const ArrayXL &v1, const ArrayXL &v2) {
    bool res = 1;
    for(size_t i=0; i < v1.size(); i++) {
        if (v1[i] != v2[i])
        {
            res = 0;
            break;
        }
    }
    return res;
}

